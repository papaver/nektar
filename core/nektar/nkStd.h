//------------------------------------------------------------------------------
// offsetof 
//  - macro to get offset of structs member variable
//  - usually address 0 is used but this will cause issues with user defined ctors
//------------------------------------------------------------------------------

#ifdef offsetof
  #define offsetof_builtin offsetof
  #undef offsetof
#endif

#define offsetof(c,m) ((size_t)&(((c*)1)->m)-1)

